@section('contenido')
<h1 style="text-align: center">QUE SOMOS?</h1>
<section style="text-align: justify">
<p>            
Somos un proyecto de extensión universitaria en la <a href="http://www.uniquindio.edu.co">Universidad del Quindío</a>, adscrito en la Facultad de Ciencias Básicas y Tecnologías en el que buscamos que los niños, niñas y jóvenes realicen un aprendizaje constructivo, didáctico y armónico hacia la motivación y orientación por un pregrado (carrera universitaria) de educación superior, estimulando las inteligencias espacial, emocional, financiera, intrapersonal, interpersonal, lingüística, naturalista, matemática, musical, y política.
</p>
<p>
	Nuestra estructura académica funciona bajo diplomados infantiles y juveniles, así:
</p>
<p>
	<a href="/inicio/semillas-preescolar">Diplomado  Mundo U - Preescolares</a>, donde los pequeños de 4 y 5 años se divierten aprendiendo ciencias y adquiriendo vocabulario en el idioma inglés.
</p>
<p>
	<a href="/inicio/semillas-primaria">Diplomado Inducción U - Primaria</a>, es el espacio para que los niños y niñas entre los 6 y 10 años, seleccionen un área entre Artes, Ciencias, Inglés o Tecnología.
</p>
<p>
	<a href="/inicio/semillas-secundaria">Diplomado Exploración U - Secundaria</a>, esta diseñado para jóvenes que cursan los grados 6° a 11° y que quieren explorar su vocación profesional a través de las diferentes opciones de pregrado que ofrece la Universidad del Quindío.
</p>
<p>
	Además el Semillero Universitario cuenta con amplia experiencia en cursos Pre-ICFES, ahora llamados Curso <a href="/inicio/pre-icfes-saber-11">Pre ICFES Saber 11°</a> y <a href="/inicio/pre-icfes-10">Curso Pre ICFES 10° </a> que prepararan a las semillas egresadas bachilleres o estudiantes de grado once para el examen de ingreso a la Educación Superior. Y para aquellos que no han podido ingresar a la Universidad, el semillero les tiene el <a href="/inicio/diplomado-experiencias-universitarias">Diplomado Experiencias Universitarias</a> que los prepara para el examen ICFES, los ambienta en la cultura universitaria y les profundiza en las áreas básicas de los primeros semestres como lo son: matemática universitaria, comprensión de textos en español e ingles, haciéndoles fácil su adaptación al mundo universitario, como una estrategia para disminuir los indices de deserción escolar universitaria.
</p>
</p>
</section>
